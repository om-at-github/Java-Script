function digit_in_word(number)

{

switch (number)

{

    case 1: console.log("One=", number);
        break;

    case 2: console.log("Two=", number);
        break;

    case 3: console.log("Three=", number);
        break;

    case 4: console.log("Four=", number);
        break;

    case 5: console.log("Five=", number);
        break;

    default: console.log("No case excuted");

}

}

digit_in_word(5)